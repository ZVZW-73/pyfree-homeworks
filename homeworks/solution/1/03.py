# Создаем пустой словарь для хранения задач
tasks = {}

# Цикл для ввода данных 3 раза
for _ in range(3):
    # Запрашиваем у пользователя дату
    date = input("Введите дату: ")
    # Запрашиваем у пользователя задачу
    problem = input("Введите задачу: ")
    # Добавляем пару "дата: задача" в словарь
    tasks[date] = problem

